    private void jButtonGetAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGetAllActionPerformed
        // TODO add your handling code here:
        java.util.List<Mahasiswa> allMhs = service.getAll();
        listID.clear();
        listNama.clear();
        for (Mahasiswa mhs : allMhs) {
            listID.addElement(String.valueOf(mhs.getId()));
            listNama.addElement(mhs.getNama());
        }
    }//GEN-LAST:event_jButtonGetAllActionPerformed

    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        listID.addElement(String.valueOf(listID.size() + 1));
        listNama.addElement(jTextFieldAdd.getText());
        
        Mahasiswa mhsAdd = new Mahasiswa(listID.size() ,jTextFieldAdd.getText());
        service.add(mhsAdd);
        jTextFieldAdd.setText("");
        service.displayAll();
    }//GEN-LAST:event_jButtonAddActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:
        int index = jListId.getSelectedIndex();
        if (index != -1){
            String selectedNama = jTextFieldEditNama.getText();
            
            String id = listID.getElementAt(index);
            Mahasiswa mhsUpdate = service.getById(Integer.parseInt(id));
            
            mhsUpdate.setNama(selectedNama);
            service.update(mhsUpdate);
            
            listNama.setElementAt(selectedNama, index);

            jTextFieldEditNama.setText("");  
            jTextFieldEditID.setText("");
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int index = jListId.getSelectedIndex();
        if (index != -1) {
            
            String id = listID.getElementAt(index);
            String nama = listNama.getElementAt(index);
            jTextFieldDeleteId.setText(id);
            jTextFieldDeleteNama.setText(nama);

            service.delete(Integer.parseInt(id));

            listID.remove(index);
            listNama.remove(index);
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonResetIndexActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResetIndexActionPerformed
        // TODO add your handling code here:
        service.resetIndeks();
        java.util.List<Mahasiswa> allMhs = service.getAll();
        listID.clear();
        listNama.clear();
        for (Mahasiswa mhs : allMhs) {
            listID.addElement(String.valueOf(mhs.getId()));
            listNama.addElement(mhs.getNama());
        }
    }//GEN-LAST:event_jButtonResetIndexActionPerformed

